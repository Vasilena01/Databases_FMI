CREATE TABLE USERS
(
	ID INT IDENTITY,
	EMAIL VARCHAR(50) NOT NULL,
	PASS VARCHAR(100) NOT NULL,
	REGIST_DATE DATE DEFAULT GETDATE()
);

CREATE TABLE FRIENDS
(
	U_ID INT,
	FRIEND_ID INT
);

CREATE TABLE WALLS
(
	U_ID INT,
	SENDER_ID INT,
	MESSAGE VARCHAR(500),
	SENDED_DATE DATE
);

CREATE TABLE GROUPS
(
	ID INT IDENTITY,
	NAME VARCHAR(50),
	DESCRIPTION VARCHAR(100) DEFAULT ''
);

CREATE TABLE GROUP_MEMBERS
(
	GROUP_NUM INT,
	MEMBER_NUM INT
);